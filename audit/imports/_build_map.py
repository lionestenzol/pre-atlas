def load_config(root: Path) -> dict[str, Any]:
    """Load atlas-map.json from <root>/, falling back to DEFAULTS for any missing key."""
    cfg_path = root / CONFIG_FILENAME
    cfg: dict[str, Any] = {k: (list(v) if isinstance(v, list) else dict(v) if isinstance(v, dict) else v)
                           for k, v in DEFAULTS.items()}
    if not cfg_path.is_file():
        return cfg
    try:
        with open(cfg_path, "r", encoding="utf-8") as f:
            user_cfg = json.load(f)
    except (json.JSONDecodeError, OSError) as exc:
        print(f"[warn] could not parse {cfg_path}: {exc}", file=sys.stderr)
        return cfg
    if not isinstance(user_cfg, dict):
        print(f"[warn] {cfg_path} root must be a JSON object", file=sys.stderr)
        return cfg
    cfg.update(user_cfg)
    return cfg


def write_starter_config(root: Path, overwrite: bool = False) -> tuple[Path, bool]:
    """Generate atlas-map.json at <root>/. Returns (path, was_written)."""
    cfg_path = root / CONFIG_FILENAME
    if cfg_path.exists() and not overwrite:
        return cfg_path, False
    with open(cfg_path, "w", encoding="utf-8") as f:
        json.dump(DEFAULTS, f, indent=2, ensure_ascii=False)
    return cfg_path, True


def port_alive(port, timeout=0.25):
    if not port:
        return False
    try:
        with socket.create_connection(("127.0.0.1", int(port)), timeout=timeout):
            return True
    except (OSError, ValueError):
        return False


def compute_churn(root, days=30):
    """git log --name-only over last N days -> {filepath: commits}."""
    try:
        result = subprocess.run(
            ["git", "log", f"--since={days}.days.ago", "--name-only", "--pretty=format:"],
            capture_output=True, text=True, cwd=str(root), timeout=30, encoding="utf-8", errors="ignore"
        )
    except (FileNotFoundError, subprocess.TimeoutExpired, OSError):
        return {}
    churn = {}
    for line in result.stdout.splitlines():
        line = line.strip().replace("\\", "/")
        if line:
            churn[line] = churn.get(line, 0) + 1
    return churn


